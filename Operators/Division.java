package Operators;

public class Division {
    public static void main(String[]args){
        int i1=16;
        int i2=4;
        float f1=3.0f;
        double d1=3.0;

        int result1=i1/i2;
        float result2=i1/f1;
        double result3=i1/d1;

        System.out.println("result1 :"+result1);
        System.out.println("result2 :"+result2);
        System.out.println("result3 :"+result3);
    }
}
