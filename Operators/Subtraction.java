package Operators;

public class Subtraction {
    public static void main(String[]args){
        int i1=14;
        int i2=10;
        float f1=2.2f;
        double d1=32;

        int result1=i1-i2;
        float result2=i1-f1;
        double result3=i1-d1;

        System.out.println("Result1="+result1);
        System.out.println("result2 :"+result2);
        System.out.println("result3 :"+result3);
    }
}
