package Operators;

public class UnaryPlusMinus {
    public static void main(String[]args){
        int i1=+20;//+is optional
        int i2=20;
        int i3=-100;
        int i4=-(i1+i2);

        System.out.println("i1="+i1);
        System.out.println("i2="+i2);
        System.out.println("i3="+i3);
        System.out.println("i4="+i4);
    }
}
