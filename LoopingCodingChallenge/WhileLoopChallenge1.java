package LoopingCodingChallenge;
/*
Use pre-decrement operator in while's boolean_expression to
 print HELLO 5times on to the console.
 */

public class WhileLoopChallenge1 {
    public static void main(String[]args){
        int count=0;
         while(--count > -6){
             System.out.println("Hello");
         }
    }
}
