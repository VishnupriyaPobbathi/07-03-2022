/**
 * class to show examples of int primitive.
 *
 * @author Vishnupriya
 */
public class IntPrimitive {
    public static void main(String[]args){
        int ip=-23456789;
        System.out.println(ip);
        ip=23456788;
        System.out.println(ip);
        ip=1_00_000;
        System.out.println(ip);

        //ip=-2345678910;//out of range, it's range is -23456789 to 23456788.
    }
}
