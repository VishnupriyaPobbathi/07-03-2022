/**
 * class to show examples of positive byte primitive.
 *
 * @author Vishnupriya
 */
public class BytePrimitive1 {
    public static void main(String[] args){
        byte bp1=32;//Decimal
        byte bp2=0b100000;//Binary Number
        byte bp3=0x20;//Hexadecimal
        byte bp4= 040;//octal

        System.out.println("Decimal NUmber="+bp1);
        System.out.println("Binary Number="+bp2);
        System.out.println("Hexadecimal Number="+bp3);
        System.out.println("Octal Number="+bp4);
    }
}
